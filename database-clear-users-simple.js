// ============================================
// CloudBase 数据库操作 - 清空 users 表（简洁版）
// 在云开发控制台 -> 数据库 -> users -> 高级代码编辑器 中运行
// ============================================

const db = cloud.database()
const _ = db.command

// ============================================
// 方法1: 直接清空 users 表（推荐）
// ============================================
db.collection('users')
  .where({
    _openid: db.RegExp({
      regexp: '.*',
      options: 'i'
    })
  })
  .remove()

// ============================================
// 方法2: 先看数量，再清空
// ============================================
// 先统计
db.collection('users').count()

// 确认后清空
db.collection('users')
  .where({
    _openid: db.RegExp({
      regexp: '.*',
      options: 'i'
    })
  })
  .remove()

// ============================================
// 方法3: 删除指定用户
// ============================================
db.collection('users')
  .where({
    _openid: 'test_user_001'
  })
  .remove()

// ============================================
// 方法4: 删除积分小于 10 的用户
// ============================================
db.collection('users')
  .where({
    points: _.lt(10)
  })
  .remove()

// ============================================
// 方法5: 删除无头像的用户
// ============================================
db.collection('users')
  .where({
    avatarUrl: _.eq('')
  })
  .remove()

// ============================================
// 方法6: 删除指定时间之前注册的用户
// ============================================
db.collection('users')
  .where({
    createdAt: _.lt(new Date('2024-01-01'))
  })
  .remove()

// ============================================
// 方法7: 删除没有记录数据的用户
// ============================================
db.collection('users')
  .where({
    totalCarbonRecords: _.eq(0)
  })
  .remove()

// ============================================
// 方法8: 删除测试用户（昵称包含"测试"）
// ============================================
db.collection('users')
  .where({
    nickName: db.RegExp({
      regexp: '测试',
      options: 'i'
    })
  })
  .remove()

// ============================================
// 方法9: 组合条件删除
// ============================================
// 删除积分小于 10 且 注册时间超过 30 天的用户
db.collection('users')
  .where({
    points: _.lt(10),
    createdAt: _.lt(new Date(Date.now() - 30 * 24 * 60 * 60 * 1000))
  })
  .remove()

// ============================================
// 方法10: 删除指定数量的用户（保留最新的N个）
// ============================================
// 先查看所有用户并按时间排序
db.collection('users')
  .orderBy('createdAt', 'asc')
  .limit(1000)
  .get()
  .then(res => {
    // 获取要删除的用户ID（例如保留最新的100个）
    const toDelete = res.data.slice(0, -100).map(user => user._id)
    
    // 批量删除
    return Promise.all(
      toDelete.map(id => db.collection('users').doc(id).remove())
    )
  })
